//* Operator Perbandingan
// Operator perbandingan merupakan operator yang hasilnyqadalah nilai boolean
// Operator perbandingan digunakan untuk membandingkan nilai nilai


// Lebih dari
// Kurang dari
// Lebih dari atau sama dengan
// Kurang dari atau sama dengan

void main() {
  var value1 = 10;
  var value2 = 5;
  
  print(value1 == value2); // Sama dengan
  print(value1 != value2); // Tidak sama dengan
  print(value1 > value2); // Lebih dari
  print(value1 < value2); // Kurang dari
  print(value1 >= value2); // Lebih dari atau sama dengan
  print(value1 <= value2); // Kurang dari atau sama dengan
}