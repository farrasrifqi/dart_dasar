//* Operator aritmatika digunakan untuk operasi seperti penjumlahan atau perkalian
void main() {
  var Value1 = 5;
  var Value2 = 3;

  print(Value1 + Value2); // + = Pertamnbahan
  print(Value1 - Value2); // -, -expression = Pengurangan atau Negatif
  print(Value1 * Value2); // * = Perkalian 
  print(Value1 / Value2); // / = Pembagian, Hasil double
  print(Value1 ~/ Value2); // ~/ = Pembagian, Hasil int
  print(Value1 % Value2); // % = Sisa bagi
}
